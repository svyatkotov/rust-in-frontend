fn main() {
    let name = "Alice";
    let age = 25;
    let height = 1.75;
    let numbers = [1, 2, 3, 4, 5];

    // 1. Базовое форматирование
    println!("1. Привет, {}! Тебе {} лет.", name, age);

    // 2. Позиционные аргументы
    println!("2. {1} лет, зовут {0}, рост {2}м", name, age, height);

    // 3. Именованные аргументы
    println!("3. Имя: {name}, возраст: {age}, рост: {height:.2}м");

    // 4. Форматирование чисел
    println!("4. Число π: {:.3}", 3.14159);
    println!("5. Двоичное: {:b}, Шестнадцатеричное: {:x}, Восьмеричное: {:o}", 255, 255, 255);

    // 5. Выравнивание и отступы
    println!("6. |{:<20}|{:^20}|{:>20}|", "левый", "центр", "правый");

    // 6. Отладка и красивое отображение
    println!("7. Отладка: {:?}", numbers);
    println!("8. Красиво: {:#?}", numbers);

    // 7. Наполнители
    println!("9. Заполнение нулями: {:05}", 42);
    // 8. Научная нотаци
    println!("10. Научная: {:e}", 1234567.89);
}
